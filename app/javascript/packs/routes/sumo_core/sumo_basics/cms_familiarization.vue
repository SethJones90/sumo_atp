          Common issues:

          Extension Badge is not glowing blue on Shopify:

          It is common to see the Sumo Chrome Extension badge not glowing blue when visiting a Shopify site.

          When troubleshooting a site, it is important to note that this is not necessarily indicative of Sumo not being installed on the site. As of now, the Sumo extension does not glow blue, even if the Sumo is installed on the site. You must manually search for the code in the site’s source code in order to determine if Sumo is installed on the site.

