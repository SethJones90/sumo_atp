          UNIT: CMS FAMILIARIZATION

          Nowadays, folks are not building their sites from scratch now that Content Management Systems have become so robust.

          Nearly 50% of Sumo users have their sites built on a CMS, which means you better be familiar with them.

          “What in the heck is a Content Management System?” Ah, great question!

          Hold on to your britches! You’re about to become an expert around these here parts.

          What is a CMS?

          Content Management Systems (CMS) are web-based applications that are designed for the creation and updating of websites. They may have themes or plugins to extend functionality, and make the management of a website simple.

          Some popular CMS’s are below:

          WordPress
          Joomla
          Drupal
          Shopify
          Squarespace
          Weebly
          BigCommerce
          Prestashop
          Magento

          ...and more!

          In order to become the best of the best, you will need to know some of the nuances in each of the CMS’s, so let’s begin!

          Here is a quick cheat sheet for identifying the installation method and some of the nuances for each CMS:

          Wordpress
          https://help.sumo.com/hc/en-us/articles/218533198-Installing-Sumo-on-Wordpress

          Installation: Manually in the &ltbody&gt;" tags of the site, or with the Sumo plugin
          Nuances: Sumo cannot be installed on wordpress.com sites - only wordpress.org

          Joomla

          https://help.sumo.com/hc/en-us/articles/218410587-Installing-Sumo-on-Joomla

          Installation: Manually within the &ltbody&gt;" tags of their index.php file.
          Nuances: Sumo cannot be installed on joomla.com sites - only joomla.org.

