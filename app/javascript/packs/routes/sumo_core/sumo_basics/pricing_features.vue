              <h4>Customer purchased subscription on wrong SiteID</h4>
              <div class="paragraph">
                <p>It is very common that a customer purchases a plan, but buys the plan on a different SiteID than they intended.</p>

                <p>We are not able to transfer purchases between SiteIDs at this time, but a customer can move the SiteID containing their purchase to any site of their choice.</p>

                <p>That being said, if the customer has written in directly after making the accidental purchase, you can cancel the plan and purchase a new plan on the desired SiteID with Sumo credit.</p>

                <p>Customer purchased subscription with wrong email address</p>

                <p>It is also common for a customer to a purchase a subscription on the wrong email address. In the event a customer wants to change the email address associated with their subscription, you will need to either change the user’s email address (not the admin email address on the SiteID), or the plan will need to be cancelled then a new one will need to be purchased with the desired email address.</p>
              </div>


              <h4>Wrong SiteID on Site</h4>
              <div class="paragraph">
                <p>Customers will also purchase subscriptions on SiteIDs, and accidentally install a separate SiteID on their site than the one that contains their purchase.</p>

                <p>When this is the case, simply instruct the customer to remove the SiteID currently installed on their site, and replace it with the SiteID that contains their purchase.</p>
              </div>

              <h4>Customer purchased plan without the features they want</h4>
              <div class="paragraph">
                <p>You will often see customers writing in, saying they do not have the features they want on their plan. While the majority of the time, you should suggest that the customer purchases the appropriate plan, if the customer is in danger of churning, you can offer to add the desired features to their current plan with a feature Product Replacement.</p>
              </div>

              <h4>Product Feature Replacements</h4>
              <div class="paragraph">
                <p>Product Replacements are an option available in admin for agents to change the original price of a plan a customer wants to purchase, as well as modify the features associated with said plan.</p>
              </div>


              <h4>Legacy Plans (multisite issues)</h4>
              <div class="paragraph">
                <p>From time to time, you will see a customer on a legacy plan write in, asking if their plan includes multiple sites. All of our plans prior to our Small, Medium, Big, and Sumo plans did not include multiple sites, and customers on these plans should be instructed to purchase a current plan if they want to use their Pro features on multiple sites.</p>
                <p>In the event that the customer is on a Small, Medium, Big, or Sumo plan (or a plan after that) you should instruct the customer to assign their desired site(s) to their subscription on the parent site.</p>
              </div>


              <h4>Resubscribing to Legacy Plans</h4>
              <div class="paragraph">
                <p>It is also common for a customer to write in, wanting to subscribe to one of their legacy plans after it has gone delinquent. In this case, you will need to inform the customer that these plans are no longer available, and they will need to purchase a current plan.</p>
              </div>


