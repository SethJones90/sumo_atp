                <p>Customers may assign their secondary sites to their subscriptions from either their primary or secondary site. In order to do this, they will need to make sure the account that purchased the subscription is an admin on both sites.</p>

                <p>Steps to assign a site to a subscription are below:</p>

                <div class="list-container">
                  <ul>
                    <li>Log into Sumo on your Primary (Pro) or Secondary site</li>
                    <li>Open the Launcher Settings menu and select Sumo Store</li>
                    <li>Click the Billing tab</li>
                    <li>Select Assign Site</li>
                    <li>Select the Secondary site you want to add to your subscription</li>
                  </ul>
                </div>

                <p><a href="https://help.sumo.com/hc/en-us/articles/115000376791" target="_blank">https://help.sumo.com/hc/en-us/articles/115000376791</a></p>

