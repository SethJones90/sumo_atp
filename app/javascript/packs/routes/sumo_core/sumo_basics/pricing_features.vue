              <h3>How Purchases Work With SiteIDs</h3>
              <div class="paragraph">

                <p>Sumo registration and purchases are SiteID based. You can register as many SiteID's as you want under your Sumo Account but the SiteID is what stores the purchase, data and settings.</p>
                <p>Here’s an example of the structure:</p>

                <p>Sumo Account (email):</p>
                <p>* SiteID 1</p>
                <p>-- Purchase</p>
                <p>-- Data</p>
                <p>-- Settings</p>

                <p>* SiteID 2</p>
                <p>-- Purchase</p>
                <p>-- Data</p>
                <p>-- Settings</p>

                <p>We do not recommend using the same Sumo SiteID/Install code on multiple websites. Doing so means all information is combined and shared. If the domain names are different you may experience bugs, as Sumo was not designed for this use.</p>
                <p>We recommend unique SiteID's per website to ensure Settings, Purchases and Data are tied to the proper websites, as nothing can be merged, transferred, or separated.</p>
                <p>If you ever need to reference this information here's the helpfile:</p>
                <p>https://help.sumome.com/hc/en-us/articles/218452548</p>
